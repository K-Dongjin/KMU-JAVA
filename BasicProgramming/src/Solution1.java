
class Solution1 {
    public String solution(int i1, double d1, int i2, double d2) {
        String str_i1 = i1 + "";
        String str_d1 = d1 + "";
        String str_i2 = i2 + "";
        String str_d2 = d2 + "";
        String answer = str_i1 + " " + str_d1 + " " + str_i2 + " " + str_d2;
        return answer;
    }
}